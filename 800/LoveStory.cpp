#include <bits/stdc++.h>
using namespace std;

int main(){
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin>>t;
    while(t--){
        string s;
        cin>>s;
        string c="codeforces";
        int count=0;
        for (int i=0;i<s.size();i++){
            if (s[i]!=c[i]){
                count++;
            }
        }
        cout<<count<<endl;
    }
    return 0;
}